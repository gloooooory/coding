class Solution{
    Node divide(int N, Node head){
        Node even = new Node(0);
        Node odd = new Node(0);
        
        Node res = even;
        Node oddHead = odd;
        
        while (head != null) {
            if (head.data % 2 == 0) {
                even.next = new Node(head.data);
                even = even.next;
            } else {
                odd.next = new Node(head.data);
                odd = odd.next;
            }
            head = head.next;
        }
        
        even.next = oddHead.next;
        
        return res.next;
    }
}